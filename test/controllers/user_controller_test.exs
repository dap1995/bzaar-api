defmodule Bzaar.UserControllerTest do
  use Bzaar.ConnCase

  alias Bzaar.User
  alias Bzaar.TestHelpers
  @valid_attrs %{active: true, email: "some content", image: "some content", name: "some content", password: "some content", surname: "some content"}
  @check_attrs %{active: true, email: "some content", image: "some content", name: "some content", surname: "some content"}
  @invalid_attrs %{}

  setup %{conn: conn} do
    {:ok, conn: put_req_header(conn, "accept", "application/json")}
  end

  test "creates and renders resource when data is valid", %{conn: conn} do
    conn = post conn, user_path(conn, :create), user: @valid_attrs
    assert json_response(conn, 201)["data"]["id"]
    assert Repo.get_by(User, @check_attrs)
  end

  test "does not create resource and renders errors when data is invalid", %{conn: conn} do
    conn = post conn, user_path(conn, :create), user: @invalid_attrs
    assert json_response(conn, 422)["errors"] != %{}
  end

